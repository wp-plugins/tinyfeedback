<h2>About this plugin</h2>
<p><em>All you need to know about tinyFeedback.</em></p>

<p>tinyFeedback is a minimalistic yet highly configurable visitor feedback plugin with a set of additional features such as Google Analytics-integration and Akismet spam filtering.</p>

<p>For more information, please visit the <a href="http://cbsmth.se/web-development/tinyfeedback-wordpress-plugin/">plugin homepage</a>.</p>
